public class pattern {
    //M.I for interview
    public static void main(String[] args) {
        int n =4;
        int m=5;
        //outer loop--rows assign
        for (int i=1;i<=n;i++){
            //inner loop--colum assign
            for (int j=1;j<=m;j++){
                System.out.print("*");
            }
            System.out.println();
        }

    }

    }
